function find_ices() {
    var alist = document.getElementsByTagName("a");
    for (a_el of alist) {
	var target = a_el.attributes["target"];
	if (target && target.value == "train") {
	    let icestring = a_el.innerHTML.split("<")[0];
	    icestring = icestring.split("\n")[0];
	    if (icestring.slice(0, 3) != "ICE")
		continue;

	    let icesplit = icestring.split(" ");
	    let zug_nr = icesplit[icesplit.length - 1];
	    console.log("found ice #" + zug_nr + "#");

	    var newspan = null;
	    for (elem of a_el.parentNode.children) {
		if (elem.className == "icetypespan")
		    newspan = elem;
	    }
	    if (!newspan) {
		let newbr = document.createElement("br");
		newspan = document.createElement("span");
		a_el.after(newbr);
		newbr.after(newspan);
		newspan.className = "icetypespan";
	    }
	    var zugtype = typedict[zug_nr];
	    if (!zugtype)
		zugtype = "???";
	    newspan.innerText = "[" + zugtype + "]";
	}
    }
}

